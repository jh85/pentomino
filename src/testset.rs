    pub fn simple_test_board(n: usize) -> Vec<Vec<usize>> {
        let num_pieces = get_num_pieces(n);
        let height: usize = match n {
            4 => 5, // 5 = 1x5
            5 => 4, // 12 = 3x4
            6 => 5, // 35 = 5x7
            7 => 12, // 108 = 9x12
            8 => 41, // 369 = 9x41
            9 => 257, // 1285 = 5x257
            10 => 95, // 4655 = 49x95
            11 => 271, // 17073 = 63x271
            _ => panic!("test_board: not supported number {}", n),
        };
        let width: usize = num_pieces / height;
        let mut b = vec![vec![0;n*width];n*height];
        let polyominoes = free_polyominos(n);
        for (k,p) in polyominoes.iter().enumerate() {
            let i = k / width;
            let j = k % width;
            for ii in 0..n {
                for jj in 0..n {
                    b[i*n + ii][j*n + jj] = if p[ii][jj] == 1 { 0 } else { 1 };
                }
            }
        }
        b
    }

    fn array2vec<const N0: usize, const N1: usize>(arr: [[usize; N1]; N0]) -> Vec<Vec<usize>> {
        arr.iter()
            .map(|row| row.to_vec())
            .collect()
    }

    fn array2vec_3d<const N0: usize, const N1: usize, const N2: usize>(arr: [[[usize; N2]; N1]; N0])
                                                                       -> Vec<Vec<Vec<usize>>> {
        arr.iter()
            .map(|plane| {
                plane.iter()
                    .map(|row| row.to_vec())
                    .collect::<Vec<Vec<usize>>>()
            })
            .collect::<Vec<Vec<Vec<usize>>>>()
    }

